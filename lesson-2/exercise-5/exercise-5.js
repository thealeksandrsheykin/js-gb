/*
5. Реализовать четыре основные арифметические операции в виде функций с двумя
   параметрами. Обязательно использовать оператор return.
*/

function sum(x,y) {
    return x + y
}
function dif(x,y) {
    return x - y
}
function mul(x,y) {
    return x * y
}
function div(x,y) {
    return x / y
}
console.log(sum(1,2))
console.log(dif(1,2))
console.log(mul(1,2))
console.log(div(1,2))
